public class ArrayExString {
    public static void main(String[] args) {

        String days[] = {"Sunday", "Monday", "Tuesday", "Wednesaday", "Thursday", "Friday", "Satarday"};
        for(int i=0; i<days.length; i++){
            System.out.println(days[i].length()+"\t"+days[i]);
        }
    }
}
